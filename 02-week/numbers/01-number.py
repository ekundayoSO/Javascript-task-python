def converNumberToString(number):
    return number

print(converNumberToString(str(42)))
print(converNumberToString(str(97)))
print(converNumberToString(str(11)))

print(type(converNumberToString(str(42))))
print(type(converNumberToString(str(97))))
print(type(converNumberToString(str(11))))

def convertStringToNumber(string):
    return string

print(convertStringToNumber(int("42")))
print(convertStringToNumber(int("97")))
print(convertStringToNumber(int("11")))

print(type(convertStringToNumber(int("42"))))
print(type(convertStringToNumber(int("97"))))
print(type(convertStringToNumber(int("11"))))

